
public class Swapping {
 
    public static void main(String[] args){
    int a, b;
    a = 24;
    b = 11;
    System.out.println("Before swapping : a is : "+a+" ,and b is :"+b);
    a = a + b;
    b = a - b;
    a = a - b;
    System.out.println("After swapping : a is : "+a+" ,and b is :"+b);
    
    }

}
